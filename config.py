G_DECAY = 0.7

# ---------------------------------------------------------------------------
# Distortion-aware C_mat (lens distortion compensation)
#
# UNDISTORT_MODE = False → build_kinematic_matrix uses the pure pinhole model
#                          (default; ignores k1..k3, p1, p2 from calib.txt).
# UNDISTORT_MODE = True  → build_kinematic_matrix accepts the dist_coeffs and
#                          constructs C_mat in DISTORTED pixel space, so that
#                          the network's F = C_mat · R prediction matches the
#                          flow actually observed at the distorted event
#                          coordinates. The skew parameter is 0 and we assume
#                          pure rotation (no translation), which collapses the
#                          per-pixel transformation to:
#                              C_mat = diag(fx, fy) · J_g(x, y) · M_omega(x, y)
#                          where (x, y) are the undistorted normalised coords
#                          recovered iteratively (OpenCV plumb-bob model).
#
# Callers should pass:
#   dist_coeffs = calib.dist if UNDISTORT_MODE else None
# to InteractingMapsThesis / InteractingMaps.
# ---------------------------------------------------------------------------
UNDISTORT_MODE = False

